<h2>이용약관</h2>

<p><strong>최종 수정일: [2024년 O월 O일]</strong></p>

<hr>

<h4>제1조 (목적)</h4>
<p>이 약관은 [사이트 이름](이하 '사이트')이 제공하는 배당 정보 및 관련 서비스(이하 '서비스')를 이용함에 있어, 사이트와 이용자의 권리와 의무, 책임사항을 규정함을 목적으로 합니다.</p>

<h4>제2조 (용어의 정의)</h4>
<p>'이용자'란 사이트에 접속하여 이 약관에 따라 사이트가 제공하는 서비스를 이용하는 모든 방문자를 의미합니다.</p>

<h4>제3조 (서비스 내용 및 저작권)</h4>
<p>① 사이트는 주식 배당과 관련된 다양한 정보를 제공합니다.</p>
<p>② 사이트가 작성한 콘텐츠, 데이터, 디자인 등 모든 저작물에 대한 저작권 및 지적재산권은 사이트에 귀속됩니다. 이용자는 사이트의 사전 서면 동의 없이 이를 영리 목적으로 복제, 배포, 전송, 출판할 수 없습니다.</p>

<h4>제4조 (이용자의 의무)</h4>
<p>이용자는 사이트의 정상적인 운영을 방해하거나, 다른 이용자의 서비스 이용을 저해하는 어떠한 행위도 하여서는 안 됩니다.</p>

<h4>제5조 (서비스 제공의 제한 및 면책)</h4>
<p><strong>① [매우 중요] 사이트에서 제공하는 모든 정보(배당률, 주가, 배당일 등)는 정보 제공을 목적으로 하며, 특정 종목의 매수 또는 매도를 추천하는 것이 절대 아닙니다.</strong></p>
<p><strong>② 사이트는 제공되는 정보의 정확성, 완전성, 최신성을 보장하지 않으며, 정보의 오류나 지연으로 인해 발생하는 어떠한 손해에 대해서도 책임을 지지 않습니다.</strong></p>
<p><strong>③ 모든 투자 결정에 대한 최종적인 책임은 투자자 본인에게 있으며, 사이트는 이용자의 투자 결과에 대하여 어떠한 법적 책임도 부담하지 않습니다.</strong></p>

<h4>제6조 (약관의 효력 및 변경)</h4>
<p>사이트는 필요한 경우 이 약관을 변경할 수 있으며, 변경된 약관은 사이트 내에 공지함으로써 효력이 발생합니다.</p>

<h4>부칙</h4>
